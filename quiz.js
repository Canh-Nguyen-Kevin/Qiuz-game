const timeRemaining = $("#time-remaining");
const timeAnswer = $("#time-answer");

//Functions
window.onload = function () {
  question.innerHTML = `Welcome to "SQUIZ GAME"! You only have 100 seconds to complete all the questions, if not, you are done for! Understand?`;
  question.style.color = "red";
};

//Timing functions
var count1 = 100;
var counter1 = setInterval(remaining, 1000);
function remaining() {
  count1 = count1 - 1;
  timeRemaining.innerHTML = count1;
  if (count1 <= 0) {
    clearInterval(counter1);
    loseTheGame();
  }
}

var count2 = 10;
var counter2 = setInterval(rush, 1000);
function rush() {
  count2 = count2 - 1;
  timeAnswer.innerHTML = count2;
  if (count2 <= 0) {
    clearInterval(counter2);
    loseTheGame();
  }
}

function loseTheGame() {
  alert("you're done for");
  clearInterval(counter2);
  clearInterval(counter1);
}

//Function with button
function answerQuestion(option) {
  const questionId = document
    .getElementById("question")
    .getAttribute("questionId");
  console.log("ques id", questionId);
  let idNextQuestion = questionId + option;
  if (idNextQuestion.length > 4) {
    if (window.confirm("Run out of questions, do you want to restart")) {
      idNextQuestion = "1a";
    } else {
      return;
    }
  }
  console.log("idques", idNextQuestion);
  const contentNextQuestion = arr.filter(
    (item) => item.id === idNextQuestion
  )[0].content;
  question.innerHTML = contentNextQuestion;
  question.setAttribute("questionId", idNextQuestion);
  count2 = 10;
  rush();
}
yesBtn.onclick = () => answerQuestion("a");
